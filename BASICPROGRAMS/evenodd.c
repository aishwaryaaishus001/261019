/*PROGRAM TO CHECK WHETHER THE NUMBER IS EVEN OR ODD*/
#include<stdio.h>
void main()
{
	int n;
	printf("\n\n\n");
	printf("Enter the number: ");
	scanf("%d",&n);
	if(n%2==0)
	{
		printf("\n\n\n");
		printf("The number is even");
	}
	else
	{
		printf("\n\n\n");
		printf("The number is odd");
	}
	printf("\n\n\n");
}
